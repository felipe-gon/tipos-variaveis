public class Operadores {
    public static void main(String[] args) {
        int numero = 5;

        numero = - numero; // Para negar um número, utilizar o operador - antes da variável
            System.out.println(numero);

        numero = numero * -1; // Para converter um que está negativo em positivo, multiplicar por -1
            System.out.println(numero);

        numero = - numero;
            System.out.println(numero);
    }
}
